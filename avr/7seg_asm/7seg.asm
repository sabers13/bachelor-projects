	  MOV R30,R18
	  LPM R29,Z
	  MOV R30,R27
	  LPM R26,Z

l3:	  SBI PORTC,0
	  SBI PORTC,1
	  OUT PORTB,R29
	  CBI PORTC,0
	  SBI PORTC,0
	  OUT PORTB,R26
	  CBI PORTC,1
	  SBI PORTC,1
	  ;OUT PORTB,R21
