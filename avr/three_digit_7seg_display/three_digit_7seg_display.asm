	  SBI PORTC,1
	  SBI PORTC,2
	  OUT PORTB,R19
	  CBI PORTC,0
	  SBI PORTC,0
	  OUT PORTB,R20
	  CBI PORTC,1
	  SBI PORTC,1
	  OUT PORTB,R21
	  CBI PORTC,2
	  RJMP l3

