	  BRLO l2
	  SUBI R16,10
	  INC R17
	  RJMP l1
l2:	  CPI R17,10
	  BRLO exit
	  SUBI R17,10
	  INC R18
	  RJMP l2
exit: LDI R31,$02
	  MOV R30,R16
	  LPM R19,Z
	  MOV R30,R17
	  LPM R20,Z
	  MOV R30,R18
	  LPM R21,Z
