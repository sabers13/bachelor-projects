	  OUT DDRA,R16
	  OUT SFIOR,R16
	  SER R16
	  OUT PORTA,R16
	  OUT DDRB,R16
	  OUT DDRC,R16
	  IN R16,PINA
	  CLR R17
	  CLR R18
