.INCLUDE "M32DEF.INC"
.ORG 0
RJMP main
.ORG $014
RJMP rout_0
.ORG $100
main:	LDI R16,$02
		LDI R17,$09
		LDI R18,49
		SER R20
		CLR R21
		LDI R23,$01
		OUT SPL,R21
		OUT SPH,R23
		SBI DDRB,0
		OUT OCR0,R18
		OUT TIMSK,R16
		SEI
		OUT TCCR0,R17
wait:	RJMP wait	

rout_0:	EOR R21,R20
		OUT PORTB,R21
		RETI	
