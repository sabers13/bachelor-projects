    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
ArrayList<String> subservices = new ArrayList<String>();
        Node pos = firstMenu.services.getNode(listOfServices.serviceID);
        if (pos.getSubservices() == null) {
            
            subservices.add(subserviceTextField.getText());
            pos.setSubservices(subservices);
        }else
        {
            subservices=pos.getSubservices();
           subservices.add(subserviceTextField.getText());
            pos.setSubservices(subservices);
        }
        subserviceTextField.setText("");
        
        JOptionPane.showMessageDialog(null, "Subservice has been added");
    }//GEN-LAST:event_addButtonActionPerformed

