    public void setTable(JTable jTable1, DefaultTableModel tblModel) {
        tblModel.addColumn("Name");
        

        String name;
        
        boolean headValue = true;
        Node temp=firstMenu.agencies.head;
        while (temp != null) {

            name = temp.name;
            

            temp = temp.next;
            if (!headValue) {
                tblModel.addRow(new Object[]{name});
            }
            headValue = false;
        }

        jTable1.setModel(tblModel);

    }
