    public void setTable(JTable jTable1, DefaultTableModel tblModel) {
        tblModel.addColumn("Name");
        tblModel.addColumn("Car Model");
        tblModel.addColumn("Customer instruction");
        tblModel.addColumn("Agency instruction");
        tblModel.addColumn("Cost");

        String name;
        String carModel;
        String customerInstruction;
        String agencyInstruction;
        double cost;
        boolean headValue = true;
        Node temp=firstMenu.services.head;
        while (temp != null) {

            name = temp.name;
            carModel = temp.carModel;
            customerInstruction = temp.customerInstruction;
            agencyInstruction = temp.agencyInstruction;
            cost = temp.cost;

            temp = temp.next;
            if (!headValue) {
                tblModel.addRow(new Object[]{name, carModel, customerInstruction, agencyInstruction, cost});
            }
            headValue = false;
        }

        jTable1.setModel(tblModel);

    }

