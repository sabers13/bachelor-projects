    private void finderButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finderButton1ActionPerformed
        boolean found = false;
        for (int indexOfUser = 0; indexOfUser < database.user.size(); indexOfUser++) {
            if (firstNameField.getText().equals(database.firstName.get(indexOfUser)) && lastNameField.getText().equals(database.lastName.get(indexOfUser))) {
                JOptionPane.showMessageDialog(null, database.firstName.get(indexOfUser) + " " + database.lastName.get(indexOfUser) + " salary is: " + database.salary.get(indexOfUser));
                found = true;
            }
        }
        if (!found) {
                JOptionPane.showMessageDialog(null, "Not found!");
            }
    }//GEN-LAST:event_finderButton1ActionPerformed

